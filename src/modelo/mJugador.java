package modelo;

/**
 *
 * @author karls
 */
public class mJugador {
        public String nombre;
    public int tipo;
    public int puntaje;

    public mJugador(String nombre, int tipo, int puntaje) {
        this.nombre = nombre;
        this.tipo = tipo;
        this.puntaje = puntaje;
    }
    
    
    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public int getTipo() {
        return tipo;
    }

    public void setTipo(int tipo) {
        this.tipo = tipo;
    }

    public int getPuntaje() {
        return puntaje;
    }

    public void setPuntaje(int puntaje) {
        this.puntaje = puntaje;
    }
}
